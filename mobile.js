// filter function
let tempFilter = [];
function filterItem(mobiles, a) {
  let filterd = mobiles.filter(function (elem) {
    return elem.brand == a;
  });

  // main display function
  filterd.forEach(function (elem) {
    tempFilter.push(elem);
  });
  displayData(tempFilter);
}
