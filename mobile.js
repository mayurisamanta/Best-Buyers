function displayData(mobiles) {
  document.querySelector("tbody").innerHTML = "";
  mobiles.forEach(function (elem) {
    let row = document.createElement("tr");

    let img = document.createElement("img");
    let imgd = document.createElement("td");
    img.src = elem.img_url;
    imgd.append(img);

    let price = document.createElement("td");
    let startwith = document.createElement("p");
    startwith.innerText = "Starting With";
    let price_1 = document.createElement("h3");
    price_1.textContent = elem.price;
    let addcart = document.createElement("button");
    addcart.textContent = "Add to Cart";
    let icon = document.createElement("img");
    icon.src = "/img/trolly.png";
    addcart.append(icon);

    price.append(startwith, price_1, addcart);

    let name = document.createElement("td");
    let name_1 = document.createElement("h4");
    name_1.innerText = elem.name;
    let content = document.createElement("img");
    content.src = "/img/rating.png";
    name.append(name_1, content);

    row.append(imgd, name, price);
    document.querySelector("tbody").append(row);
  });
}

